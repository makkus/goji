package org.bestgrid.goji.exceptions;

public class UserInitException extends Exception {

	public UserInitException() {
	}

	public UserInitException(String arg0) {
		super(arg0);
	}

	public UserInitException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UserInitException(Throwable arg0) {
		super(arg0);
	}

}
